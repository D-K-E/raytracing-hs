module RandomFn where
{-
import System.Random


random_double :: Maybe Float -> Maybe Float -> Float

random_double mi ma           = fst (randomR mi ma)
random_double Nothing Nothing = fst (randomR 0.0 1.0)

random_int :: Maybe Int -> Maybe Int -> Int

random_int mi ma           = fst (randomR mi ma)
-}
